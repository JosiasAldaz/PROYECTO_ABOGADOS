        String ruta;
        FileNameExtensionFilter filtrado = new FileNameExtensionFilter("JPG", "PNG", "jpg", "png");
        int respuesta = nuevo.showOpenDialog(this);
        if (respuesta == JFileChooser.APPROVE_OPTION) {
            ruta = nuevo.getSelectedFile().getPath();
            ruta1.setText(ruta);
        }
