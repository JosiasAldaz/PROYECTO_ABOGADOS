        if (a) {  // a es una variable boolean en true
            jPasscontra.setEchoChar((char) '*');
            jPasscontra2.setEchoChar((char) '*');
            mostrarasis.setVisible(true);
            ocultarasis.setVisible(false);
            a = true;
        } else {
            jPasscontra.setEchoChar(i); // i es el char
            a = true;
        }
